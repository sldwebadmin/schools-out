//    Tree stays inside school region (x=4096..6336). East fence at x=6312 is 272px east. ✓
{ const t=findW('tree',6280,544); t.x=6020; const c=findC(6290,548); c.x=6030; fixes++; }

// 3. School-east tree (6280,1070): canopy@(6290,1074,r=60) clips science-wing visual by 50px.
//    Move 260px west to x=6020. Canopy nearest science-wing corner = 80.6px > 60. ✓
{ const t=findW('tree',6280,1070); t.x=6020; const c=findC(6290,1074); c.x=6030; fixes++; }

// 4. Shopping tree (7780,3520): canopy@(7790,3524,r=60) clips market visual bottom by 48px.
