import { readFileSync, writeFileSync } from 'fs';

const jsText  = readFileSync('./src/world/mapdata.js', 'utf8');
const jsonText = jsText.replace(/^[\s\S]*?export default /, '').replace(/;\s*$/, '');
const data    = JSON.parse(jsonText);
const { walls: ws, canopies: cs } = data;

function findW(type, x, y) {
  const w = ws.find(w => w.type === type && w.x === x && w.y === y);
  if (!w) throw new Error(`wall ${type}@(${x},${y}) not found`);
  return w;
}
function findC(x, y) {
  const c = cs.find(c => c.x === x && c.y === y);
  if (!c) throw new Error(`canopy@(${x},${y}) not found`);
  return c;
}

let fixes = 0;

// 1. Neighbourhood tree (3760,5880): overlaps yard fence@(3760,5868,10×122)
//    Move 20px west to x=3740 — right edge (3760) just touches fence left edge; no logical overlap.
//    Canopy (3750,5884,r=52): 97px clear of house visual left (x=3847). ✓
{ const t=findW('tree',3760,5880); t.x=3740; const c=findC(3770,5884); c.x=3750; fixes++; }

// 2. School-east tree (6280,544): canopy@(6290,548,r=56) clips science-wing visual by 34px.
