/**
 * fix-mapdata-4.mjs — fix visual-bound conflicts invisible to the logical validator
 *
 * Problems fixed:
 *  1. Park trees (4420,2380) and (4420,3300) — canopies extend 34–38px into main road
 *     even though trunks are clear.  Move trunks 40px further west so canopy right
 *     edge clears road left edge (x=4448) with 6px margin.
 *  2. Neighbourhood tree (3720,4660) — canopy extends 12px south into HY1 (y=4480).
 *     Move 20px further south so canopy clears road top with 8px margin.
 *  3. Tree (3796,5880) near player's house — canopy overlaps house visual by 11px.
 *     Move 36px further west so canopy clears house left visual edge (x=3847) by 25px.
 *  4. SW-block fence (2640,5580) — pickets reach y=5559, 1px inside house bottom
 *     at y=5560.  Move 22px south.
 *  5. SE-block fence (4640,5580) — same pattern.  Move 22px south.
 *
 * node scripts/fix-mapdata-4.mjs
 */
import { readFileSync, writeFileSync } from 'fs';

const jsText  = readFileSync('./src/world/mapdata.js', 'utf8');
const jsonText = jsText.replace(/^[\s\S]*?export default /, '').replace(/;\s*$/, '');
const data    = JSON.parse(jsonText);
const { walls: ws, canopies: cs } = data;

function findW(type, x, y) {
  const w = ws.find(w => w.type === type && w.x === x && w.y === y);
  if (!w) throw new Error(`wall ${type}@(${x},${y}) not found`);
  return w;
}
function findC(x, y) {
  const c = cs.find(c => c.x === x && c.y === y);
  if (!c) throw new Error(`canopy@(${x},${y}) not found`);
  return c;
}

let fixes = 0;

// 1a. Park tree (4420,2380): canopy (4430,2384,r=52) right edge = 4482 → inside road (x=4448)
//     Move to (4380,2380): canopy right edge = 4390+52=4442, 6px clear of road
{ const t=findW('tree',4420,2380); t.x=4380; const c=findC(4430,2384); c.x=4390; fixes++; }

// 1b. Park tree (4420,3300): canopy (4430,3304,r=56) right edge = 4486 → inside road
//     Move to (4380,3300): canopy right edge = 4390+56=4446, 2px clear of road
{ const t=findW('tree',4420,3300); t.x=4380; const c=findC(4430,3304); c.x=4390; fixes++; }

// 2.  Neighbourhood tree (3720,4660): canopy (3730,4664,r=56) bottom 4720; nearest
//     HY1 point is y=4620, distance=44 < 56 → 12px into road
//     Move to (3720,4680): canopy (3730,4684), distance to HY1 bottom = 64 > 56
{ const t=findW('tree',3720,4660); t.y=4680; const c=findC(3730,4664); c.y=4684; fixes++; }

// 3.  Player-house tree (3796,5880): canopy (3806,5884,r=52) dist to house visual
//     left (x=3847) = 41px < 52 → overlaps by 11px
//     Move to (3760,5880): canopy (3770,5884), dist = 77px > 52
{ const t=findW('tree',3796,5880); t.x=3760; const c=findC(3806,5884); c.x=3770; fixes++; }

// 4.  SW-block fence: logical 20px below house bottom, but pickets reach 21px north
//     fence@(2640,5580) pickets at y=5559, house bottom=5560 → 1px visual clip
//     Move south 22px to y=5602: pickets now at y=5581 > 5560
{ const f=findW('fence',2640,5580); f.y=5602; fixes++; }

// 5.  SE-block fence: same issue
{ const f=findW('fence',4640,5580); f.y=5602; fixes++; }

const header = '// AUTO-GENERATED by scripts/make-mapdata.mjs — do not edit by hand.\n';
writeFileSync('./src/world/mapdata.js',
  header + 'export default ' + JSON.stringify(data, null, 2) + ';\n');
console.log(`Patched src/world/mapdata.js — ${fixes} fixes applied.`);
