//       Move 70px west so canopy clears treehouse corner by 8px
{ const t=findW('tree',870,3040); t.x=800; const c=findC(880,3044); c.x=810; fixes++; }

// ── 2. Perimeter tree (960,3040): canopy (r=48) overlaps Left treehouse top-right corner
//       Move 40px north so canopy clears treehouse top edge by 8px
{ const t=findW('tree',960,3040); t.y=3000; const c=findC(970,3044); c.y=3004; fixes++; }

// ── 3. Perimeter tree (1260,3300): canopy covers NPC at (1250,3310) and could reach South treehouse
